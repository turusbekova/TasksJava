package Loop;

public class Loop2 {
    public static void main(String[] args) {

       for (int i = 2; i <= 7; i++){
           System.out.println(i + " *" + " 2 = " + i * 2);
       }
        //4 --> 2*2
        //6 --> 3*2
        //8 --> 4*2
        //10 --> 5*2
        //12 --> 6*2
        //14 --> 7*2




    }
}
